package _24_02_2026;     //static varibales

public class Variables {
	public static byte b=10;
	public static short s=20;
	public static  int i=30;
	public static long l=40;
	public static float f=50;
	public static double d=60;
	public static boolean b1=true;
	public static char ch='a';
	
	
	public static void print() {
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(b1);
		System.out.println(ch);
		
	
	}

	public static void main(String[] args) {
		print();

	}

}
